        <h2>How does the service work?</h2>
    </u>
    <br>
    <p>Once you have completed your order you will be added to our system and our staff will be out to visit your property on your next bin collection date. Our teams work similar routes to the bin men and will attend your property once your bin has been emptied. This will ensure that when we arrive to clean the bin it will be empty and ready for us to clean. Please try to avoid putting new rubbish in the bin until after we have arrived. <br>
        When we arrive at the property a member of our team will come on to your property to collect the bin for cleaning. They will then bring the bin to one of our specialised vans and proceed to clean it. If you are a new customer, we will affix a round sticker to the front of the bin and write your house number on the sticker. We do this to make the bin easily identifiable to us for the future. We ask that you leave the sticker on the bin to avoid any confusion for our staff. <br>
        The bin will be cleaned on the back of our van, using our hydraulic system to raise the bin on to the back of our van. Our power washing system automatically deep cleans the bins ensuring they are cleaned to a high standard every time. The vans are designed to collect all the dirty water and store it In a tank that is built on to the van. This means we are leaving no trace on the ground outside your property and we can filter and dispose of the water correctly at the end of the day. <br>
        After the bin has been power washed, our team will dry the bin by hand, making sure that there is no water left in the bin. Once the bin has been dried they will spray the bin with our deodoriser which leaves the bin smelling fresh and clean. <br>
        After we have cleaned the bin our team will then return the bin to your property and leave a slip of paper between the lid of the bin which lets you know that the bin has been cleaned and when your next clean will be.
        <br>
        The service is the same for both our commercial and residential customers. For commercial customers who are not open during the day, such as restaurants and bars, we will clean the bin during the day after it is emptied and post a receipt through your letter box.
